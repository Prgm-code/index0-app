export function FileIcon({ filename, className = "" }: FileIconProps) {
  const extension = filename.split(".").pop()?.toLowerCase() || "";

  // Encontrar el tipo de archivo basado en la extensión
  const fileType = Object.entries(fileTypes).find(([, type]) =>
    type.extensions.includes(extension)
  );

  // Si no se encuentra un tipo específico, usar el icono de documento por defecto
  const icon = fileType ? fileType[1].icon : fileTypes.document.icon;

  return React.cloneElement(
    icon as React.ReactElement<SVGProps<SVGSVGElement>>,
    {
      className: `${
        (icon as React.ReactElement<SVGProps<SVGSVGElement>>).props.className
      } ${className}`,
    }
  );
}
